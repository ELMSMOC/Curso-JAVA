public class Car {
    int seatsNum;
    String color;
    int doorCount;
    String brand;
    char type;
    boolean isAuto;

    Car(int seatsNum, String color, int doorCount, String brand, char type, boolean isAuto){
        this.seatsNum = seatsNum;
        this.color = color;
        this.doorCount = doorCount;
        this.brand = brand;
        this.type = type;
        this.isAuto= isAuto;
    }
}